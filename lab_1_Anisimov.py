# Завдання 1
print("Введіть чотири числа:")
a = float(input("Введіть перше число (ціле або дробове): "))
b = float(input("Введіть друге число (ціле або дробове): "))
c = int(input("Введіть третє число (ціле): "))
d = int(input("Введіть четверте число (ціле): "))

# Завдання 2
results = [
    a + b,  # додавання
    a - b,  # віднімання
    a * b,  # множення
    a / b,  # ділення
    a ** b, # піднесення до ступеня
    c // d, # цілочисленне ділення
    c % d   # остача від ділення
]

# Завдання 3
print(f"Кількість елементів у списку: {len(results)}")
print("Парні елементи списку:")
for i in range(len(results)):
    if i % 2 == 0:
        print(results[i])

# Завдання 4
results[1], results[4] = results[4], results[1]
print("Список після зміни місцями елементів:")
print(results)

